from turtle import *
from time import sleep
t = Turtle()

t.speed(0)

def desenha_retangulo(x,y, larg, alt, color):
    t.pu()
    t.goto(x, y)
    t.pd()
    t.color(color)
    t.begin_fill()
    t.fd(larg)
    t.lt(90)
    t.fd(alt)
    t.lt(90)
    t.fd(larg)
    t.lt(90)
    t.fd(alt)
    t.end_fill()
    t.color('white')
    t.rt(90)
    t.begin_fill()
