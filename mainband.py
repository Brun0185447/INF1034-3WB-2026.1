def desenha_retangulocirc(x,y, larg, alt, color):
    t.color(color)
    for _ in range(2):
        t.rt(90)
        t.fd(alt)
        t.rt(90)
        t.fd(larg)
    t.pu()
    t.goto(x, y)
    t.pd()
    t.color('red')
    t.begin_fill()
    t.circle(50)
    t.end_fill()
