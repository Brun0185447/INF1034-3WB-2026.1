from turtle import *
from time import sleep
t = Turtle()
t.pu()
t.goto(300, 200)
t.pd()
t.color("red")
t.begin_fill()

t.fd(100)
t.lt(90)
t.fd(150)
t.lt(90)
t.fd(100)
t.lt(90)
t.fd(150)
t.end_fill()

t.color('white')
t.rt(90)
t.begin_fill()
t.fd(100)
t.rt(90)
t.fd(150)
t.rt(90)
t.fd(100)
t.rt(90)
t.fd(150)
t.end_fill()

t.color('green')
t.pu()
t.rt(90)
t.fd(100)
t.pd()
t.begin_fill()
t.fd(100)
t.rt(90)
t.fd(150)
t.rt(90)
t.fd(100)
t.rt(90)
t.fd(150)
t.end_fill()
sleep(3)
t.clear()

t.color('black')
for _ in range(2):
    t.rt(90)
    t.fd(300)
    t.rt(90)
    t.fd(150)
t.pu()
t.goto(10, 280)
t.pd()
t.color('red')
t.begin_fill()
t.circle(50)
t.end_fill()
sleep(3)
t.clear()

t.begin_fill()
for _ in range(2):
    t.fd(150)
    t.rt(90)
    t.fd(100)
    t.rt(90)
t.end_fill()
t.color('green')
t.begin_fill()
t.lt(90)
t.fd(300)
t.rt(90)
t.fd(50)
t.rt(90)
t.fd(300)
t.end_fill()
t.color('white')
t.lt(90)
t.fd(50)
t.lt(90)
t.fd(300)
t.lt(90)
t.fd(50)
t.color('black')
t.begin_fill()
t.rt(180)
t.pu()
t.fd(50)
t.pd()
t.rt(90)
t.fd(300)
t.lt(90)
t.fd(50)
t.lt(90)
t.fd(300)
t.lt(90)
t.fd(50)
t.end_fill()
sleep(3)
t.clear()

# bandeira turquia
